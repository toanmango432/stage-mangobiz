/**
 * Supabase Server Client for Next.js Server Components, Route Handlers, and Server Actions.
 *
 * Uses @supabase/ssr with Next.js cookies() for cookie-based auth session management.
 * Call createClient() inside each server function/component — it returns a new client each time.
 */

import { createServerClient } from '@supabase/ssr';
import { cookies } from 'next/headers';
import type { Database } from '@/services/supabase/types';

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient<Database>(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) => {
              cookieStore.set(name, value, options);
            });
          } catch {
            // setAll can fail in Server Components where cookies are read-only.
            // This is expected — session refresh is handled by middleware instead.
          }
        },
      },
      global: {
        headers: {
          'X-Client-Info': 'mango-online-store/1.0.0',
        },
      },
    }
  );
}
